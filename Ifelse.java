class Ifelse{  //creating Ifelse class
public static void main(String[]args) { 
		
int x=5;   //variable x, int type
int y=3;
if(x+y<10){
System.out.println("x+y is less than 10");
}     //end of if statement
else{
System.out.println("x+y is greater than 10");
}    //end of else statement

}     //end of main method
}	//end of class
